package main;

public class Data {
	int number;
	String kodeKaryawan;
	String nama;
	String jenisKelamin;
	String jabatan;
	int gaji;
	
	public Data(int number, String kodeKaryawan, String nama, String jenisKelamin, String jabatan, int gaji) {
		super();
		this.number = number;
		this.kodeKaryawan = kodeKaryawan;
		this.nama = nama;
		this.jenisKelamin = jenisKelamin;
		this.jabatan = jabatan;
		this.gaji = gaji;
	}
	
}
